public class Employee {
    // String with character array
    public static void main(String[] args) {
        char[] ch = {'S','a','l','m','a'};
        String firstName = new String(ch);
        System.out.println(firstName);
        // Output : Salma
        String lastName = "Shaik";
        System.out.println(lastName);
        // Output = Shaik
    }
}